// Object

// criando um objeto
const person = {
  name: "Wenderson",
  age: 20,
  weight: 76,
  isAdmin: true,
};

// exibindo todos as propriedades do objeto
console.log(person);

// capturando apenas uma propriedade do objeto
console.log(person.name);

// interpolação com objetos
console.log(`${person.name} tem ${person.age} anos`);
